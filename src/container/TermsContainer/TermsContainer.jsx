						<div>①회사는 회원이 서비스를 통하여 등록한 게시물이나, 다른 회원과 주고받은 쪽지 등과 관련하여 어떠한 민형사상 책임도 부담하지 않으며, 회원이 등록한 게시물이나 다른 회원과 주고받은 쪽지가 다음 각 호 어느 하나에 해당하는 경우에는 사전통지 없이 이를 삭제할 수 있습니다.</div>
